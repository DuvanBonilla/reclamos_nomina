<?php
require_once("../model/conexion.php");


$conexion = new Conexion();
$conMysql = $conexion->conMysql();
// ---------------------------------------------------------- ---------------------------------------------------------- ------------------------------------------------------------------------------------------------------------
// buscar todos los usuarios
// ---------------------------------------------------------- ---------------------------------------------------------- ------------------------------------------------------------------------------------------------------------
$sql = "SELECT * FROM usuarios";
$result = $conMysql->query($sql);
// ---------------------------------------------------------- ---------------------------------------------------------- ------------------------------------------------------------------------------------------------------------
// actualizar usuario
// ---------------------------------------------------------- ---------------------------------------------------------- ------------------------------------------------------------------------------------------------------------

if ($result->num_rows > 0)
    while ($row = $result->fetch_assoc()) {
        echo "<tr>";
        echo "<td>" . $row['codigo'] . "</td>";
        // ---------------------------------------------------------- ---------------------------------------------------------- ------------------------------------------------------------------------------------------------------------
        // rol
        // ---------------------------------------------------------- ---------------------------------------------------------- ------------------------------------------------------------------------------------------------------------
        if ($row['id_rol'] == 1) {
            echo "<td> Administrador </td>";
        } else if ($row['id_rol'] == 2) {
            echo "<td> Coordinador </td>";
        } else if ($row['id_rol'] == 3) {
            echo "<td> Costos </td>";
        } else if ($row['id_rol'] == 4) {
            echo "<td> Nomina </td>";
        }
        // ---------------------------------------------------------- ---------------------------------------------------------- ------------------------------------------------------------------------------------------------------------
        // zona
        // ---------------------------------------------------------- ---------------------------------------------------------- ------------------------------------------------------------------------------------------------------------
        $arrayZonas = array(
            1 => "Uniban",
            2 => "Banacol",
            3 => "Cfs",
            4 => "Banafrut",
            5 => "Conserba",
            6 => "Fyffes",
            7 => "Smitco",
            8 => "Spsm",
            9 => "Simbacol",
        );
        $zona = $arrayZonas[$row['id_zona']];
        if ($zona) {
            echo "<td>" . $zona . "</td>";
        }
        // ---------------------------------------------------------- ---------------------------------------------------------- ------------------------------------------------------------------------------------------------------------
        // actualizar
        // ---------------------------------------------------------- ---------------------------------------------------------- ------------------------------------------------------------------------------------------------------------

        echo "<td><button class='popup-button update-user-button ' data-codigo='" . $row['codigo'] . "' style='background-color: blue;'><i class='fas fa-sync-alt'></i> </button></td>";
        // ---------------------------------------------------------- ---------------------------------------------------------- ------------------------------------------------------------------------------------------------------------
        // eliminar
        // ---------------------------------------------------------- ---------------------------------------------------------- ------------------------------------------------------------------------------------------------------------
        if ($row['estado'] == 1) {
            echo "<td><button class='popup-button estado-user' data-estado='" . $row['estado'] . "' data-codigo='" . $row['codigo'] . "' style='background-color: green;'><i class='fas fa-check-circle'></i> </button></td>";
        } else {
            echo "<td><button class='popup-button estado-user' data-estado='" . $row['estado'] . "' data-codigo='" . $row['codigo'] . "' style='background-color: red;'><i class='fas fa-times-circle'></i> </button></td>";
        }
    }
